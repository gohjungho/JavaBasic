class FlowEx22 {
    public static void main(Strign[] args) {
        int[] arr = 0;
        int sum = 0;

        for(int i = 0; i < arr.length; i++);
            system.out.printf("%d ", arr[i]);
        System.out.println();

        for(int tmp : arr) {
            System.out.printf("%d", tmp);
            sum += tmp;
        }
        System.out.println();
        System.out.println("sum =  " + sum);
    }
}
